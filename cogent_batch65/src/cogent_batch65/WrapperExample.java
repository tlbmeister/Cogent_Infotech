package cogent_batch65;

public class WrapperExample {

	public static void main(String[] args) {
		
		//int,byte, short, long, float, double boolean, char
		
		int a=2;
		byte b=34;
		char c='c';
		boolean d=true;
		double f=1.5;
		float g=1.25f;
		long h=100;
		short i=10;
		
		Integer j=Integer.valueOf(a);
		Byte k=Byte.valueOf(b);
		Character l=Character.valueOf(c);
		Boolean m=Boolean.valueOf(d);
		Double n=Double.valueOf(f);
		Float o=Float.valueOf(g);
		Long p=Long.valueOf(h);
		Short q=Short.valueOf(i);
		
		int aa=j.intValue();
		byte bb=k.byteValue();
		char cc=l.charValue();
		boolean dd=m.booleanValue();
		double ff=n.doubleValue();
		float gg=o.floatValue();
		long hh=p.longValue();
		short ii=q.shortValue();
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		System.out.println(j);
		System.out.println(k);
		System.out.println(l);
		System.out.println(m);
		System.out.println(n);
		System.out.println(o);
		System.out.println(p);
		System.out.println(q);		
		System.out.println(aa);
		System.out.println(bb);
		System.out.println(cc);
		System.out.println(dd);
		System.out.println(ff);
		System.out.println(gg);
		System.out.println(hh);
		System.out.println(ii);






		System.out.println();

	}

}
